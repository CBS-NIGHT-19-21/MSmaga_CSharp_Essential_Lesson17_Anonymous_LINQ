
namespace Task_Additional_Calculator
{
    static class Calculator
    {
        public static dynamic Add (dynamic operand1, dynamic operand2) => operand1 + operand2;
        public static dynamic Sub(dynamic operand1, dynamic operand2) => operand1 - operand2;
        public static dynamic Mul(dynamic operand1, dynamic operand2) => operand1 * operand2;
        public static dynamic Div(dynamic operand1, dynamic operand2) => operand1 / operand2;
    }
}
